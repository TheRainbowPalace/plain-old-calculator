from math import *
import math
from random import random as rand


def sqrt(value, n):
    return value ** (1/n)


def sqrt2(value):
    return math.sqrt(value)


def sqrt3(value):
    return value ** (1/3)


def ln(value):
    return math.log(value, math.e)


def deg(value):
    return math.degrees(value)
